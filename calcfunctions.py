################CALCULATOR FUNCTIONS################
#creates basic functionality for unitary operators

def add(x,y):
    result = (x + y)
    print(result)

def sub(x,y):
    result = (x- y)
    print(result)

def mult(x,y):
    result = (x*y)
    print(result)

def div(x,y):
    result = (x/y)
    print(result)